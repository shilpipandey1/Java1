package JavaBasics;

public class DataTypes {

	public static void main(String[] args) 
	
	{
		
		//byte
	  byte b=10;
	  byte c= 120;
	  byte f= -90;
	  byte t = 0;
	  
	  System.out.println(b);
	  
	  //Short
	  short s = 1000;
	  short s1= 10;
	  System.out.println(s+s1);
	  
	  //long 
	  
	  long l=10;
	  long distance = 1212121567888L;
	  System.out.println(distance);
	   
	   //float 4 bytes
	  
	  float t4= 12.33f;
	  
	  float t5= (float) 123.44;
	  System.out.println(t4);
	  System.out.println(t5);
	  
	  //double  8 bytes
	  
	  double d1= 12.33;
	  double d2= 1.333445;
	  System.out.println(d1);
	  System.out.println(d2);
	  
	  
       
	}

}
